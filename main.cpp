#include<iostream>
#include<ctime>
#include<chrono>
#include<thread>
using namespace std;

const int POSSIBLECARDS = 21;
int dealerCardVal = 0;
int playerCardVal = 1;
int balance = 1000;
int dealerCard[POSSIBLECARDS]{};
int playerCard[POSSIBLECARDS]{};

void fillArray() {
	for (int i = 0; i < POSSIBLECARDS; i++) {
		dealerCard[i] = rand() % 9 + 2;
		playerCard[i] = rand() % 9 + 2;
	}
}

void game() {
        char choice;
        while (true) {
        int tempPlayerSum = 0, tempDealerSum = 0;
        dealerCardVal = 0;
        playerCardVal = 1;

        for (int i = 0; i <= dealerCardVal; i++) {
            tempDealerSum += dealerCard[i];
        }
        for (int i = 0; i <= playerCardVal; i++) {
            tempPlayerSum += playerCard[i];
        }
        
        int insurance = 0;
        int currentBet = 0;
        cout << "[+] Balance: " << balance << endl;
        cout << "[?] How much you wanna bet?: ";
        cin >> currentBet;
        cout << "[?] Take insurance?: ";
        cin >> choice;
        if (choice == 'y'|| choice == 'Y') {
            insurance = 1;
        }

        if (currentBet <= 0 || currentBet > balance) {
            cout << "[-] Invalid." << endl;
            continue;
        }


        if (balance == 0) {
            cout << "[-] Youre broke.. But i will give you 500 bucks!" << endl;
            balance += 500;
            continue;
        }

        while (true) {
            cout << "[+] Dealer hand: ";
            for (int i = 0; i <= dealerCardVal; i++) {
                cout << dealerCard[i] << " ";
            }
            cout << endl;
            
            cout << "[+] Player hand: ";
            for (int i = 0; i <= playerCardVal; i++) {
                cout << playerCard[i] << " ";
            }
            cout << endl;
            
            cout << "[?] Player sum: " << tempPlayerSum << endl;
            
            if (insurance == 1) {
                if (tempDealerSum + dealerCard[1] == 21) {
                    cout << "[I] Dealer won with blackjack." << endl;
                }
            }
            if (tempPlayerSum > 21) {
				std::this_thread::sleep_for(std::chrono::milliseconds(500));
                cout << "[-] Overdraw!" << endl;
                balance -= currentBet;
                break;
            }
            
            if (tempPlayerSum == 21) {
				std::this_thread::sleep_for(std::chrono::milliseconds(500));
                cout << "[777] Blackjack!" << endl;
                balance += currentBet * 2;
                if (tempDealerSum == 21) {
					std::this_thread::sleep_for(std::chrono::milliseconds(500));
                    cout << "[/] Push! Tie game." << endl;
                } else {
					std::this_thread::sleep_for(std::chrono::milliseconds(500));
                    cout << "[+] You won!" << endl;
                    balance += currentBet * 2;
                }
                break;
            }
            
            cout << "[?] Enter your next move (Hit - H, Stand - S, Bet more - B End the game - X): ";
            cin >> choice;
            
            if (choice == 'H' || choice == 'h') {
                playerCardVal++;
                if (playerCardVal < POSSIBLECARDS) {
                    tempPlayerSum += playerCard[playerCardVal];
                }
            }
            if (choice == 'B' || choice == 'b') {
                int a;
                cout << "[?] How much are we betting?: ";
                cin >> a;
                currentBet += a;
            }
            else if (choice == 'S' || choice == 's') {
                cout << "[+] Dealer's turn..." << endl;
				std::this_thread::sleep_for(std::chrono::milliseconds(500));
                for (int i = 0; i <= dealerCardVal; i++) {
                    cout << "[+] Current dealer's hand: " << dealerCard[i] << " ";
                }
				std::this_thread::sleep_for(std::chrono::seconds(2));
                cout << endl;
                
                while (tempDealerSum < 17) {
                    dealerCardVal++;
                    if (dealerCardVal < POSSIBLECARDS) {
                        tempDealerSum += dealerCard[dealerCardVal];
                        cout << "[+] Dealer draws: " << dealerCard[dealerCardVal] 
                             << " | Total: " << tempDealerSum << endl;
							 std::this_thread::sleep_for(std::chrono::seconds(1));
                    }
                }
                
                if (tempDealerSum > 21) {
					std::this_thread::sleep_for(std::chrono::milliseconds(500));
                    cout << "[+] Dealer busted! You won!" << endl;
                    balance += currentBet * 2;
                }
                else if (tempDealerSum > tempPlayerSum) {
					std::this_thread::sleep_for(std::chrono::milliseconds(500));
                    cout << "[-] Dealer wins with " << tempDealerSum << " vs your " << tempPlayerSum << endl;
                    balance -= currentBet;
                }
                else if (tempPlayerSum > tempDealerSum) {
					std::this_thread::sleep_for(std::chrono::milliseconds(500));
                    cout << "[+] You win with " << tempPlayerSum << " vs dealer's " << tempDealerSum << endl;
                    balance += currentBet * 2;
                }
                else {
					std::this_thread::sleep_for(std::chrono::milliseconds(500));
                    cout << "[=] Push! It's a tie." << endl;
                }
                break;
            }
            else if (choice == 'X' || choice == 'x') {
                cout << "[+] Game ended." << endl;
            }
            cout << endl;
        }
        
        cout << endl << "[?] Play again? (y/n): ";
        cin >> choice;
        if (choice == 'N' || choice == 'n') {
            break;
        }
        
        fillArray();
    }
}

int main() {
    srand(time(NULL));
    fillArray();
    game();
    cin.ignore();
    cin.get();
    return 0;
}
